/**
  * @brief This function handles TIM2 global interrupt.
  */
void TIM2_IRQHandler(void)
{
  /* USER CODE BEGIN TIM2_IRQn 0 */

  /* USER CODE END TIM2_IRQn 0 */
  HAL_TIM_IRQHandler(&htim2);
  /* USER CODE BEGIN TIM2_IRQn 1 */
	timer2_counter+=1;
	switch(timer2_counter)
	{
		case 1:
		{
      TIM1->CCR2 = 100; // 12v 6  // pil 80 mavi
			break;
		}
		case 2: 
		{
			HAL_ADC_Start_IT(&hadc1);
			led2_dark= ADC_BUF[0];
			break;
		}
		case 3: 
		{
      TIM1->CCR2 = 0;
			break;
		}
		case 4: 
		{
			HAL_ADC_Start_IT(&hadc1);
			led1_light= ADC_BUF[0];
			break;
		}
		case 5: 
		{
			TIM1->CCR3 = 205;   // 12v 84 // pil 91 beyaz
			break;
		}
		case 6: 
		{
			HAL_ADC_Start_IT(&hadc1);
			led1_dark= ADC_BUF[0];
			break;
		}
		case 7: 
		{
			TIM1->CCR3 = 0;
			break;
		}
		case 8:			
		{
			HAL_ADC_Start_IT(&hadc1);
			led2_light= ADC_BUF[0];
			result1=led1_light-led1_dark;
			result2=led2_light-led2_dark;
			timer2_counter=0;
			value1++;
			break;
		}
	}
  /* USER CODE END TIM2_IRQn 1 */
}

